package py.edu.uaa.poo.mtalavera;

public class Lector {
	private Integer numeroLector;
	private String nombre;
	private String apellido;
	private Integer telefonoContacto;
	private String tipoLector;
	public Integer getNumeroLector() {
		return numeroLector;
	}
	public void setNumeroLector(Integer numeroLector) {
		this.numeroLector = numeroLector;
	}
	public String getNombre() {
		return nombre;
	}
	public void setNombre(String nombre) {
		this.nombre = nombre;
	}
	public String getApellido() {
		return apellido;
	}
	public void setApellido(String apellido) {
		this.apellido = apellido;
	}
	public Integer getTelefonoContacto() {
		return telefonoContacto;
	}
	public void setTelefonoContacto(Integer telefonoContacto) {
		this.telefonoContacto = telefonoContacto;
	}
	public String getTipoLector() {
		return tipoLector;
	}
	public void setTipoLector(String tipoLector) {
		this.tipoLector = tipoLector;
	}
	

}
